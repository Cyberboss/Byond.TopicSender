namespace Byond.TopicSender
{
	/// <summary>
	/// The type of a <see cref="TopicResponse"/>.
	/// </summary>
	public enum TopicResponseType
	{
		/// <summary>
		/// Unknown response type.
		/// </summary>
		UnknownResponse,

		/// <summary>
		/// <see cref="string"/> response type.
		/// </summary>
		StringResponse,

		/// <summary>
		/// <see cref="float"/> response type.
		/// </summary>
		FloatResponse,
	}
}
